angka1 = int (input("Masukkan angka pertama:"))
operator = (input ("Masukkan operator yang akan anda gunakan:"))
angka2 = int (input("Masukkan angka kedua:"))

if operator == "+":
    print (angka1 + angka2)

elif operator == "-": 
    print (angka1 - angka2)

elif operator == "*":
    print (angka1 * angka2)

elif operator == "/":
    print (angka1 / angka2)